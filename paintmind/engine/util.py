def customized_collate_fn(batch):

    collate_fn = {}
    if len(batch) < 2:
        for key, value in batch[0].items():
            collate_fn[key] = [value]
    else:

        for i, dd in enumerate(batch):
            if i < 1:
                for key, value in dd.items():
                    collate_fn[key] = [value]
            else:
                for key, value in dd.items():
                    collate_fn[key].append(value)
    
    return collate_fn


